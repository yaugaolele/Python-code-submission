# 在Python中，程序接收用户输入的输入的功能即是输入
# 语法： input("提示文字")
# 特点：
#   在程序执行input时，等待用户输入，输入完成后才能继续向下执行。
#   在Python中input接收用户输入后，一般存到变量中，方便使用。
#   在Python中input会把接收到的任意用户输入的数据都当做字符串来进行处理。
name = input("请输入姓名：")
print(name)

age = input("请输入你的年龄：")
print(age)

